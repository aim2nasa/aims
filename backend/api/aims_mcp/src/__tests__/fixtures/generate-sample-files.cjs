const fs = require('fs');
const path = require('path');

// 간단한 PDF 생성 함수 (외부 라이브러리 없이)
function createMinimalPDF(textContent, outputPath) {
  // PDF 기본 구조
  const lines = textContent.split('\n');
  const textLines = lines.map((line, i) =>
    `BT /F1 10 Tf 50 ${700 - i * 12} Td (${escapeText(line)}) Tj ET`
  ).join('\n');

  const streamContent = `
BT
/F1 12 Tf
50 750 Td
(AIMS E2E Test Document) Tj
ET
${textLines}
`;

  const stream = streamContent.trim();
  const streamLength = Buffer.byteLength(stream, 'utf-8');

  const pdf = `%PDF-1.4
1 0 obj
<< /Type /Catalog /Pages 2 0 R >>
endobj
2 0 obj
<< /Type /Pages /Kids [3 0 R] /Count 1 >>
endobj
3 0 obj
<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Contents 4 0 R /Resources << /Font << /F1 5 0 R >> >> >>
endobj
4 0 obj
<< /Length ${streamLength} >>
stream
${stream}
endstream
endobj
5 0 obj
<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>
endobj
xref
0 6
0000000000 65535 f
0000000009 00000 n
0000000058 00000 n
0000000115 00000 n
0000000266 00000 n
0000000${String(350 + streamLength).padStart(3, '0')} 00000 n
trailer
<< /Size 6 /Root 1 0 R >>
startxref
${400 + streamLength}
%%EOF`;

  fs.writeFileSync(outputPath, pdf);
  console.log(`Created: ${outputPath}`);
}

function escapeText(text) {
  return text
    .replace(/\\/g, '\\\\')
    .replace(/\(/g, '\\(')
    .replace(/\)/g, '\\)')
    .replace(/[^\x00-\x7F]/g, '?'); // Non-ASCII를 ?로 대체 (기본 PDF는 한글 미지원)
}

