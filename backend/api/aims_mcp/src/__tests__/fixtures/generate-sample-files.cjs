const filesDir = path.join(__dirname, 'files');
const outputDir = path.join(__dirname, 'files');

// 텍스트 파일 목록
const textFiles = [
  'sample_insurance_certificate',
  'sample_annual_report',
  'sample_pension_plan',
];

// PDF 생성
textFiles.forEach(name => {
  const txtPath = path.join(filesDir, `${name}.txt`);
  const pdfPath = path.join(outputDir, `${name}.pdf`);

  if (fs.existsSync(txtPath)) {
    const content = fs.readFileSync(txtPath, 'utf-8');
    createMinimalPDF(content, pdfPath);
  }
});

